"use client";
import React from "react";
import SetSpeedSummary, { SpeedSummary } from "./SetSpeedSummary";

type OptimizedCombo = any; // use seu tipo real
type Props = { combos: OptimizedCombo[]; monstersById: Record<number,string>; };

function aggregateSpeed(combos: any[]): SpeedSummary[] {
  const map = new Map<string,{sets:string[], spd:number, runs:number, eff:number}>();
  for (const c of combos) {
    const sets: string[] = (c.requiredSets || c.sets || []).slice(0,2); // atÃƒÆ’Ã†â€™Ãƒâ€ Ã¢â‚¬â„¢ÃƒÆ’Ã¢â‚¬Â ÃƒÂ¢Ã¢â€šÂ¬Ã¢â€žÂ¢ÃƒÆ’Ã†â€™ÃƒÂ¢Ã¢â€šÂ¬Ã…Â¡ÃƒÆ’Ã¢â‚¬Å¡Ãƒâ€šÃ‚Â© 2 sets
    const key = [...sets].sort().join("+");
    if (!key) continue;
    const acc = map.get(key) || { sets, spd:0, runs:0, eff:0 };
    acc.spd += (c.delta?.spd ?? 0);
    acc.runs += 1;
    acc.eff  += (typeof c.eff === "number" ? c.eff : 0);
    map.set(key, acc);
  }
  return [...map.entries()].map(([k,v],i)=>({
    id: k+"#"+i,
    label: k,
    sets: v.sets,
    spd: Math.round(v.spd),           // vocÃƒÆ’Ã†â€™Ãƒâ€ Ã¢â‚¬â„¢ÃƒÆ’Ã¢â‚¬Â ÃƒÂ¢Ã¢â€šÂ¬Ã¢â€žÂ¢ÃƒÆ’Ã¢â‚¬Å¡Ãƒâ€šÃ‚Âª pode trocar por mÃƒÆ’Ã†â€™Ãƒâ€ Ã¢â‚¬â„¢ÃƒÆ’Ã¢â‚¬Â ÃƒÂ¢Ã¢â€šÂ¬Ã¢â€žÂ¢ÃƒÆ’Ã†â€™ÃƒÂ¢Ã¢â€šÂ¬Ã…Â¡ÃƒÆ’Ã¢â‚¬Å¡Ãƒâ€šÃ‚Â©dia/mÃƒÆ’Ã†â€™Ãƒâ€ Ã¢â‚¬â„¢ÃƒÆ’Ã¢â‚¬Â ÃƒÂ¢Ã¢â€šÂ¬Ã¢â€žÂ¢ÃƒÆ’Ã†â€™ÃƒÂ¢Ã¢â€šÂ¬Ã…Â¡ÃƒÆ’Ã¢â‚¬Å¡Ãƒâ€šÃ‚Â¡ximo
    runs: v.runs,
    eff: v.runs ? v.eff/v.runs : undefined,
  })).sort((a,b)=> b.spd - a.spd);
}

